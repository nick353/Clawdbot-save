#!/bin/bash
# バックテストをサブエージェントで実行

# Clawdbotのsessions_spawnを呼び出す簡易ラッパー
# 実際にはClawdbot内部から sessions_spawn() を使用

cat << 'EOF'
📊 バックテストをサブエージェントで実行

使い方（Discordで）:
---
@Nichika0823 バックテストを実行して
（自動的にサブエージェントが起動され、結果のみ報告されます）
---

または、明示的に:
---
/spawn task="仮想通貨バックテスト（5分足、1時間足）を実行して結果をサマリーで報告" label="backtest"
---

サブエージェントの特徴:
- メインセッションとは独立
- 大量の出力を別セッションで処理
- 完了後、結果のみメインセッションに報告
- cleanup="delete" で自動削除（トークン節約）

期待される効果:
- メインセッション: 500トークン（結果報告のみ）
- サブエージェント: 50,000トークン（使い捨て）
- メインセッションのトークン削減: 99%
EOF
